/* O que são variáveis?
    Variáveis são "caixinhas" onde podemos guardar informações para serem usadas posteriormente no código. Elas possuem um nome (identificador) e um valor associado.
    Exemplo:
    let nome = "João"; // Aqui, "nome" é a variável que guarda o valor "João"

    Tipos de variáveis em JavaScript:
    1. var: Escopo global ou de função. Pode ser redeclarada e atualizada.
    2. let: Escopo de bloco. Pode ser atualizada, mas não redeclarada no mesmo escopo.
    3. const: Escopo de bloco. Não pode ser atualizada nem redeclarada. Deve ser inicializada na declaração.
    Exemplo de uso:
    let idade = 25;
    const pi = 3.14;
    var cidade = "São Paulo";

    Boas práticas:
    - Use nomes descritivos para as variáveis.
    - Prefira
    let e const ao invés de var para evitar problemas de escopo.
    - Use const para valores que não irão mudar.

    O que são operadores?
    Operadores são símbolos que realizam operações em valores ou variáveis. Eles podem ser classificados em diferentes tipos:
    1. Operadores Aritméticos: +, -, *, /, %, ++, --
    2. Operadores de Atribuição: =, +=, -=, *=, /=, %=
    3. Operadores de Comparação: ==, ===, !=, !==, >, <, >=, <=
    4. Operadores Lógicos: &&, ||, !
    5. Operadores de String: + (concatenação)
*/